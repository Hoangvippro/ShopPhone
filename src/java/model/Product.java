package model;

/**
 *
 * @author ASUS
 */
public class Product {
    String pid;
    String image;
    String pname;
    int quantity;
    double price;
    int status;
    int cateID;

    public Product() {
    }

    public Product(String pid, String image, String pname, int quantity, double price, int status, int cateID) {
        this.pid = pid;
        this.image = image;
        this.pname = pname;
        this.quantity = quantity;
        this.price = price;
        this.status = status;
        this.cateID = cateID;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public int getCateID() {
        return cateID;
    }

    public void setCateID(int cateID) {
        this.cateID = cateID;
    }
    
}
